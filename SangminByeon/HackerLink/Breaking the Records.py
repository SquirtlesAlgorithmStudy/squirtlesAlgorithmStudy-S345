def breakingRecords(scores):
    # Write your code here
    minimum, maximum = scores[0],scores[0]
    min_cnt, max_cnt = 0,0
    
    for score in scores:
        if score < minimum:
            minimum = score
            min_cnt +=1
        elif score > maximum:
            maximum = score
            max_cnt +=1
    
    return [max_cnt,min_cnt]

